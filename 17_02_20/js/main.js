let flat = (data, depth = 1) => {
	let arr = [];
	data.forEach(element => {
		if(Array.isArray(element)) {
			arr.push(...element);
		} else {
			arr.push(element);
		}
	});
	if (depth === 1) {
		return arr;
	} else {
		return flat( arr, depth-1 )
	}
}
